// Add

function add(num1, num2) {
  return num1 + num2;
}

// Substract

function substract(num1, num2) {
  return num1 - num2;
}

// Multiply

function multiply(num1, num2) {
  return num1 * num2;
}

// Divide

function divide(num1, num2) {
  return num1 / num2;
}

// Modulo

function modulo(num1, num2) {
  return num1 % num2;
}
